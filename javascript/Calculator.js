function sum(a, b) {
  return a + b;
}

function sub(a, b) {
  return a - b;
}

function mul(a, b) {
  return a * b;
}

function div(a, b) {
  return a / b;
}

console.log(sum(10, 2));
console.log(sub(0, 2));
console.log(sub(2, 10));
console.log(div(10, 2));
console.log(div(10, 0));
console.log(mul(10, 2));
